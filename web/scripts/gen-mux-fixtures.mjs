/**
 * Generates the mux-allocation parity fixtures for the mod's JUnit tests:
 *
 *   src/test/resources/mux/mux_alloc_fixtures.txt
 *
 * Each scenario is the TS computeMuxAllocation() output (web/src/mux.ts) for a set of payload
 * sizes.  MuxAllocationParityTest asserts the Java MuxAllocator produces the IDENTICAL
 * allocation — the invariant that keeps web-exported schematic LOOM headers consistent with the
 * MG banners the mod generates when it re-runs the allocation at state import.  A divergence
 * corrupts muxed receivers in-game ("Src size is incorrect").
 *
 *   node web/scripts/gen-mux-fixtures.mjs   (run from the repo root or from web/)
 */
import { build } from 'esbuild';
import { writeFile, mkdir } from 'node:fs/promises';
import { tmpdir } from 'node:os';
import { join, dirname, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const webDir  = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const repoDir = resolve(webDir, '..');
const outFile = join(repoDir, 'src/test/resources/mux/mux_alloc_fixtures.txt');

async function load(p) {
  const o = join(tmpdir(), `gen-mux-${Date.now()}-${Math.random().toString(36).slice(2)}.mjs`);
  await build({ entryPoints: [join(webDir, p)], bundle: true, format: 'esm', outfile: o, platform: 'node', logLevel: 'silent' });
  return import(pathToFileURL(o).href);
}
const { computeMuxAllocation } = await load('src/mux.ts');
const { CAPACITY }             = await load('src/codec-mode.ts');

// Codecs by their JAVA enum names (the Java side has no CARPET_SHADE_BANNERS alias).
const CODECS = ['BANNER', 'CARPET', 'CARPET_SHADE', 'CARPET_BANNERS', 'CARPET_BANNERS_SHADE'];

// Deterministic LCG so regeneration is reproducible.
let seed = 20260711;
const rand = () => (seed = (seed * 1103515245 + 12345) & 0x7fffffff) / 0x7fffffff;
const randInt = (lo, hi) => lo + Math.floor(rand() * (hi - lo + 1));

const scenarios = [];

// The field-bug shape: composite lossy payloads (incompressible, over budget) + blank donors.
scenarios.push({ name: 'user-1x2-composite-4donors', codec: 'CARPET_SHADE',
                 sizes: [13633, 13632, 0, 0, 0, 0] });

// Mixed over/under budget around the exact threshold.
for (const codec of CODECS) {
  const b = CAPACITY[codec];
  scenarios.push({ name: `threshold-${codec}`, codec,
                   sizes: [b + 1, b, b - 1, 0, Math.floor(b / 2), b + 5000] });
}

// Receivers leaning on partial spare of real (non-blank) art tiles.
scenarios.push({ name: 'carpet-partial-spare', codec: 'CARPET',
                 sizes: [9000, 8000, 4000, 100, 8176, 12000] });

// Multi-receiver grid with large overflow.
scenarios.push({ name: 'cbs-3x3-multi', codec: 'CARPET_BANNERS_SHADE',
                 sizes: [20000, 15482, 26000, 1000, 15000, 30000, 0, 500, 15400] });

// Unresolvable overflow → rollback path + unresolved > 0.
scenarios.push({ name: 'cb-unresolved-rollback', codec: 'CARPET_BANNERS',
                 sizes: [40000, 60000, 13000, 13400] });

// Broad random coverage: 3 scenarios per codec, sizes spanning blank to ~2× budget.
for (const codec of CODECS) {
  const b = CAPACITY[codec];
  for (let s = 0; s < 3; s++) {
    const n = randInt(4, 12);
    const sizes = Array.from({ length: n }, () => {
      const r = rand();
      if (r < 0.25) return 0;                        // blank donor
      if (r < 0.65) return randInt(1, b);            // under budget
      return randInt(b + 1, b * 2);                  // receiver
    });
    scenarios.push({ name: `random-${codec}-${s}`, codec, sizes });
  }
}

const lines = ['# Generated by web/scripts/gen-mux-fixtures.mjs — DO NOT EDIT.',
               '# TS computeMuxAllocation output; MuxAllocationParityTest asserts Java equality.'];
for (const sc of scenarios) {
  const { roles, unresolved } = computeMuxAllocation(sc.sizes, sc.codec);
  lines.push(`scenario ${sc.name}`);
  lines.push(`codec ${sc.codec}`);
  lines.push(`sizes ${sc.sizes.join(',')}`);
  lines.push(`unresolved ${unresolved}`);
  for (const r of roles) {
    const guests = r.guests.length
      ? r.guests.map(g => `${g.rxTi}:${g.rxOffset}:${g.len}`).join(';')
      : '-';
    lines.push(`tile ${r.ti} ${r.role} own=${r.ownBytes} guests=${guests}`);
  }
  lines.push('end');
}

await mkdir(dirname(outFile), { recursive: true });
await writeFile(outFile, lines.join('\n') + '\n');
const receivers = lines.filter(l => l.includes(' receiver ')).length;
console.log(`mux fixtures: ${scenarios.length} scenarios (${receivers} receiver tiles) → ${outFile}`);
